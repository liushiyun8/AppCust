package com.eke.cust.tabmine.mytask_activity;

public class MyTaskDetailNodeInfo {
	private String missionid;
	private long time;
	private String detail;
	private String tasknum;
	private String successtasknum;
	public String getMissionid() {
		return missionid;
	}
	public void setMissionid(String missionid) {
		this.missionid = missionid;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getTasknum() {
		return tasknum;
	}
	public void setTasknum(String tasknum) {
		this.tasknum = tasknum;
	}
	public String getSuccesstasknum() {
		return successtasknum;
	}
	public void setSuccesstasknum(String successtasknum) {
		this.successtasknum = successtasknum;
	}
	
}

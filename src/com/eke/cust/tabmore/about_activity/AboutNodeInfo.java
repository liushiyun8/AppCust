package com.eke.cust.tabmore.about_activity;

public class AboutNodeInfo {
	private String Name;
	private String Site;
	private String Intro;
	private String m400;
	private String Tel;
	private String APPEmpHeadVer;
	private String APPEmpUrl;
	private String APPCustHeadVer;
	private String APPCustUrl;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getSite() {
		return Site;
	}
	public void setSite(String site) {
		Site = site;
	}
	public String getIntro() {
		return Intro;
	}
	public void setIntro(String intro) {
		Intro = intro;
	}
	public String getAPPEmpHeadVer() {
		return APPEmpHeadVer;
	}
	public void setAPPEmpHeadVer(String aPPEmpHeadVer) {
		APPEmpHeadVer = aPPEmpHeadVer;
	}
	public String getAPPEmpUrl() {
		return APPEmpUrl;
	}
	public void setAPPEmpUrl(String aPPEmpUrl) {
		APPEmpUrl = aPPEmpUrl;
	}
	public String getAPPCustHeadVer() {
		return APPCustHeadVer;
	}
	public void setAPPCustHeadVer(String aPPCustHeadVer) {
		APPCustHeadVer = aPPCustHeadVer;
	}
	public String getAPPCustUrl() {
		return APPCustUrl;
	}
	public void setAPPCustUrl(String aPPCustUrl) {
		APPCustUrl = aPPCustUrl;
	}
	public String getM400() {
		return m400;
	}
	public void setM400(String m400) {
		this.m400 = m400;
	}
	public String getTel() {
		return Tel;
	}
	public void setTel(String tel) {
		Tel = tel;
	}
}
